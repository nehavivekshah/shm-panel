                        FOREIGN KEY (domain_id) REFERENCES domains(id) ON DELETE CASCADE,
                        INDEX idx_client_id (client_id),
                        INDEX idx_status (status)
                    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci
